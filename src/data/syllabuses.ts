export interface Topic {
  id: string;
  name: string;
  completed: boolean;
}

export interface Subject {
  id: string;
  name: string;
  topics: Topic[];
}

export interface Syllabus {
  id: string;
  name: string;
  subjects: Subject[];
}

const createTopics = (topicNames: string[]): Topic[] => {
  return topicNames.map((name, index) => ({
    id: `topic-${index}`,
    name,
    completed: false,
  }));
};

